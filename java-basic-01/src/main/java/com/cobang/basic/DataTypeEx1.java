package com.cobang.basic;

public class DataTypeEx1 {
    public static void main(String[] args) {
      //기본형 - 정수형 타입
      
      //변수 선언
      byte byteTypeNum;
      //값 할당
      byteTypeNum = 10;
      System.out.println("byteTypeNum = " + byteTypeNum);
      
      //변수 선언 및 값 할당 (초기화)
      short shortTypeNum = 1;
      System.out.println("shortTypeNum = " + shortTypeNum);
      
      int intTypeNum = 200; //int형 리터럴
      System.out.println("intTypeNum = " + intTypeNum);

      long longTypeNum = 100L; //long형 리터럴
      System.out.println("longTypeNum = " +longTypeNum);
    }
}
