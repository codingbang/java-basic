package com.cobang.basic;

public class OperatorEx1 {
    public static void main(String[] args) {
      float num1 =10;
      double num2 = 5;
      
      double result = num1 + num2;
      System.out.println(result);

      result = num1 - num2;
      System.out.println(result);

      result = num1 * num2;
      System.out.println(result);
    }
}
