package com.cobang.basic;

public class OperatorEx2 {
    public static void main(String[] args) {
      int num1 = 10, num2 = 5;

      int result = num1 / num2;
      System.out.printf("%d / %d = %d%n",num1,num2,result);

      result = num1 % num2;
      System.out.printf("%d %% %d = %d%n",num1,num2,result);
    }
}
